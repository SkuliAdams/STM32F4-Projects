// Given the main loop's counter, set LED's to display counter in binary.
void setPins(uint8_t counter)
{
	// Scale bits of counter to appropriate bit for each LED
	// In reverse order in my configuration
	uint16_t pin1Set = ((counter & 0x1) >> 0) * LED4_Pin;
	uint16_t pin2Set = ((counter & 0x2) >> 1) * LED3_Pin;
	uint16_t pin3Set = ((counter & 0x4) >> 2) * LED2_Pin;
	uint16_t pin4Set = ((counter & 0x8) >> 3) * LED1_Pin;

	uint16_t pinsToSet = pin1Set | pin2Set | pin3Set | pin4Set;
	uint16_t pinsToReset = (~pinsToSet) & pins;

	HAL_GPIO_WritePin(GPIOE, pinsToSet, GPIO_PIN_SET);
	HAL_GPIO_WritePin(GPIOE, pinsToReset, GPIO_PIN_RESET);
}
